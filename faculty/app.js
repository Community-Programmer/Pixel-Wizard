function createFacultyCards() {
    const container = document.getElementById("faculty-container");
    facultyData.forEach((faculty) => {
      const card = document.createElement("div");
      card.className = "faculty-card";
  
      const facultyInfo = document.createElement("div");
      facultyInfo.className = "faculty-info";
  
      const photo = document.createElement("img");
      photo.src = faculty.photo;
      photo.alt = faculty.name;
      photo.className = "faculty-photo";
  
      const name = document.createElement("h2");
      name.className = "faculty-name";
      name.textContent = faculty.name;
  
      const department = document.createElement("p");
      department.className = "faculty-department";
      department.textContent = faculty.department;
  
      const education = document.createElement("p");
      education.className = "faculty-education";
      education.textContent = `Education: ${faculty.education}`;
  
      const contactDiv = document.createElement("div");
      contactDiv.className = "faculty-contact";
  
      const emailLink = document.createElement("a");
      emailLink.href = `mailto:${faculty.email}`;
      emailLink.innerHTML = `ðŸ“§ ${faculty.email}`; // Email icon and text
      emailLink.ariaLabel = `Email ${faculty.name}`;
  
      const phoneLink = document.createElement("a");
      phoneLink.href = `tel:${faculty.phone}`;
      phoneLink.innerHTML = `ðŸ“ž ${faculty.phone}`; // Phone icon and text
      phoneLink.ariaLabel = `Call ${faculty.name}`;
  
      const emailParagraph = document.createElement("p");
      emailParagraph.appendChild(emailLink);
  
      const phoneParagraph = document.createElement("p");
      phoneParagraph.appendChild(phoneLink);
  
      contactDiv.appendChild(emailParagraph);
      contactDiv.appendChild(phoneParagraph);
  
      facultyInfo.appendChild(photo);
      facultyInfo.appendChild(name);
      facultyInfo.appendChild(department);
      facultyInfo.appendChild(education);
      facultyInfo.appendChild(contactDiv);
  
      const facultyDetails = document.createElement("div");
      facultyDetails.className = "faculty-details";
  
      // Adding professor details
      if (faculty.professor_details) {
        faculty.professor_details.forEach((detail) => {
          if (detail.name) {
            const detailName = document.createElement("p");
            detailName.className = "faculty-detail-name";
            detailName.textContent = `Name: ${detail.name}`;
            facultyDetails.appendChild(detailName);
          }
  
          if (detail.position) {
            const detailPosition = document.createElement("p");
            detailPosition.className = "faculty-detail-position";
            detailPosition.textContent = `Position: ${detail.position}`;
            facultyDetails.appendChild(detailPosition);
          }
  
          if (detail.department) {
            const detailDepartment = document.createElement("p");
            detailDepartment.className = "faculty-detail-department";
            detailDepartment.textContent = `Department: ${detail.department}`;
            facultyDetails.appendChild(detailDepartment);
          }
  
          if (detail.institution) {
            const detailInstitution = document.createElement("p");
            detailInstitution.className = "faculty-detail-institution";
            detailInstitution.textContent = `Institution: ${detail.institution}`;
            facultyDetails.appendChild(detailInstitution);
          }
  
          if (detail.experience) {
            const experienceHeader = document.createElement("h4");
            experienceHeader.textContent = "Experience";
            facultyDetails.appendChild(experienceHeader);
  
            const experienceList = document.createElement("ul");
            detail.experience.forEach((exp) => {
              const listItem = document.createElement("li");
              listItem.textContent = exp;
              experienceList.appendChild(listItem);
            });
            facultyDetails.appendChild(experienceList);
          }
  
          if (detail.research_interests) {
            const researchHeader = document.createElement("h4");
            researchHeader.textContent = "Research Interests";
            facultyDetails.appendChild(researchHeader);
  
            const researchList = document.createElement("ul");
            detail.research_interests.forEach((interest) => {
              const listItem = document.createElement("li");
              listItem.textContent = interest;
              researchList.appendChild(listItem);
            });
            facultyDetails.appendChild(researchList);
          }
        });
      }
  
      const facultyContent = document.createElement("div");
      facultyContent.className = "faculty-content";
      facultyContent.appendChild(facultyDetails);
  
      if (faculty.link) {
        const linkButton = document.createElement("a");
        linkButton.href = faculty.link;
        linkButton.className = "faculty-link";
        linkButton.textContent = "View Profile";
        linkButton.target = "_blank"; // Opens the link in a new tab
  
        facultyContent.appendChild(linkButton);
      }
  
      card.appendChild(facultyInfo);
      card.appendChild(facultyContent);
  
      container.appendChild(card);
    });
  }
  
  document.addEventListener("DOMContentLoaded", createFacultyCards);
  